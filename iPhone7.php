<div class="heading" ;> INTRODUCTION </div>
<pre>
The iPhone 7 Plus hardly needs an introduction. Apple's latest always get the attention no
matter how much or how little the company has changed. An aura of controversy has always 
been part of Apple's marketing and some might argue that the polarized opinions actually 
work in their favor.

And the iPhone 7 duo tops the polarizing scale this season thanks to ditching the standard 
audio jack and the iconic hardware key. It seems this doesn’t get in the way of the iPhone 
providing superb performance and superb user experience for yet another year in a row. 
We’ve already brought you the iPhone 7 review, and now it’s time for its bigger sibling.
</pre>
<img  src="./images/iph_4.jpg" style="width:100%">		
<pre>
Indeed,a lot has changed since last year, but you can tell Apple is holding something back. 
Whether the company is keeping its best game for the iPhone's tenth anniversary next year, 
or just don't want to give it all at once, it doesn't really matter. If it weren't for the 
new Black editions, you can hardly tell the new iPhones from the previous generation. Same 
shape, same looks, same big screen bezels.

The list of omissions is shrinking with each year, but it's still far from a short one. 
But then again, opting for Apple has its ups and downs, and the latter don't come cheap. 
In order to keep iOS free of bugs, glitches, and throttles, Apple still poses lots of
restrictions to both developers and users.

But these are hardly a surprise to anyone, so let's not dwell on what can't be changed 
right now. Stays with us right after the break to see what's new, what got better, and 
what's gone for good.
</pre>
<div class="heading">Apple iPhone 7 Plus 360-degree spin</div>
<pre>
On the outside, little has changed since the first Plus model - the iPhone 6 Plus. 
The 6 Plus, 6s Plus, and 7 Plus all share a similar footprint at 158.2 x 77.9 x 7.3 mm 
though they weigh differently. The 6 Plus is the lightest at 172g, the new aluminum alloy, 
and 3D Touch tech added 20g up to 192g to the 6s Plus, while the new iPhone 7 Plus has 
shed some weight down to 188g.
</pre>
<div class="heading">Design and build quality</div>
<pre>
We didn't get the best deal with the new generation iPhones, that's for sure. While the
antenna bands have been refined for better looks, the design is pretty much the same for
the third year in a row. The iPhone 7 Plus looks almost identical to the iPhone 6 and 6s
Plus, and if it weren't for the new dual camera on the back, it's hardly distinguishable
from the other Pluses.
</pre>
<div class="heading">CONECTIVITY</div>
<img  src="./images/iph_5.jpg" style="width:100%">
<pre>

Of course, refurbished looks don't translate into a mediocre vibe. On the contrary,Apple's
iPhone 7 Plus is possibly one of the most beautiful and best-built devices on the market. 
Its unibody is made of sturdy airplane-grade aluminum,the front 2.5D ion-strengthened glass
is scratch-resistant and covered with oleophobic layer for smudge protection, while the 
camera is shielded with a sapphire piece.

The most notable changes in the design are the lack of the analog audio jack and the new 
dual camera. There are also two new paint jobs - Matte Black and Jet Black, replacing the
previous Space Gray. Unfortunately, the Jet Black is very prone to scratches, acknowledged
even by Apple,and as intended more like a limited eye-candy edition, rather than a 
mainstream purchase.
</pre>
<img  src="./images/iph_7.jpg" style="width:100%">
<pre>

The highlight of the iPhone 7 Plus is on the back - the dual 12MP camera with optical image
stabilization. Unlike Huawei and its P9 or Honor 8, Apple chose to opt for a regular 
wide-angle snapper (12MP, f/1.8) and a telephoto cam (12MP, f/2.8). There is also a new 
brighter quad-LED true-tone flash to complement the new setup.
</pre>
